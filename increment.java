public class increment {
    public static void main(String args[]) {
        //pre increment 
        int a = 4; 
        int b = ++a;
        System.out.println("A="+a);
        System.out.println("B="+b);

         //post increment 
         int c = 9; 
         int d = c++;
         System.out.println("C="+c);
         System.out.println("D="+d);

        
    }
    
}
